        var childBox8 = Box('h')
        var childBox9 = Box('v')
        var childBox10 = Box('v')
        var childBox11 = Box('v')
        masterBox.nestBox(childBox1)
        childBox1.nestBox(childBox2)
        childBox2.nestBox(childBox3)
        childBox2.nestBox(childBox4)
        childBox1.nestBox(childBox5)
        childBox1.nestBox(childBox6)
        masterBox.nestBox(childBox7)
        masterBox.nestBox(childBox8)
        masterBox.nestBox(childBox9)
        childBox9.nestBox(childBox10)
        childBox9.nestBox(childBox11)
        masterBox.resetGrid()
        masterBox.printGrid()
    }

