package blake.nestedBox

class TestRunner { // BOXES MUST BE NESTED IN A DFS METHOD (follow each nesting to its end before backtracking)
    fun runTest1() {
        var masterBox = Box('h') // Alignment of a box with no children doesn't matter, can be 'h' or 'v'
        masterBox.resetGrid() // must reset grid before attempting to print to allow multiple tests to be run
        masterBox.printGrid()
    }

    fun runTest2() {
        var masterBox = Box('h')
        var childBox1 = Box('v')
        var childBox2 = Box('h')
        var childBox3 = Box('v')
        var childBox4 = Box('v')
        var childBox5 = Box('h')
        var childBox6 = Box('h')
